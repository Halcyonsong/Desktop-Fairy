/**
 * 桌面精灵相关可配置项。
 *
 * 集中管理的原因：
 *   - 避免魔法数字散落在 store / 组件中，难以追溯
 *   - 便于 UI 直接绑定 min/max/step 等约束
 *   - 后续若需要做配置中心化迁移，只需改这一处
 */
export const fairyConfig = {
  /**
   * 精灵显示缩放配置。
   * 范围 0.5 - 1.0，步长 0.02，默认 0.6。
   */
  scale: {
    default: 0.6,
    min: 0.5,
    max: 1.0,
    step: 0.02,
  },

  /**
   * 自动闲聊触发时间配置（毫秒）。
   *
   * 约束（项目硬性约定）：
   *   - 范围：30 秒 - 5 分钟（30000 - 300000 ms）
   *   - 步长：30 秒（30000 ms）
   *   - 默认：3 分钟（180000 ms）
   *
   * 语义：用户在该时长内无任何交互后，桌面精灵会自动触发一次临时闲聊。
   * 同时该值也作为两次自动触发之间的冷却时间（避免连续打扰）。
   */
  idleTrigger: {
    default: 3 * 60_000, // 180000 ms = 3 分钟
    min: 30_000,         // 30 秒
    max: 5 * 60_000,     // 5 分钟
    step: 30_000,        // 30 秒
  },
} as const;
